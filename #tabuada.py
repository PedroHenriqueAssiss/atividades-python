n1 = int(input('Digite um numero:'))
contador = 1
print('tabuada multiplicação:')
while contador <= 10:
    print(n1,'x',contador,'=',n1 * contador)
    contador = contador + 1
contador = 1
print('tabuada adição:')
while contador <= 10:
    print(n1,'+',contador,'=',n1 + contador)
    contador = contador + 1
contador = 1
print('tabuada de menos:')
while contador <= 10:
    print(n1,'-',contador,'=',n1 - contador)
    contador = contador +1
contador = 1
print('tabuada de divisão:')
while contador <= 10:
    print(n1,'/',contador,'=',n1 / contador)
    contador = contador +1